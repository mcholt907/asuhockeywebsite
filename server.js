function determineNationality(hometown) {
  if (!hometown || hometown === '-') return 'USA';
  const h = hometown.toUpperCase();

  // European/International codes
  const europeMap = {
    'SVK': 'SVK', 'SLOVAKIA': 'SVK',
    'CZE': 'CZE', 'CZECH': 'CZE',
    'SWE': 'SWE', 'SWEDEN': 'SWE',
    'FIN': 'FIN', 'FINLAND': 'FIN',
    'RUS': 'RUS', 'RUSSIA': 'RUS',
    'GER': 'GER', 'GERMANY': 'GER',
    'LAT': 'LAT', 'LATVIA': 'LAT',
    'BLR': 'BLR', 'BELARUS': 'BLR',
    'SUI': 'SUI', 'SWITZERLAND': 'SUI',
    'AUT': 'AUT', 'AUSTRIA': 'AUT',
    'GBR': 'GBR', 'UK': 'GBR'
  };

  for (const [key, code] of Object.entries(europeMap)) {
    if (h.includes(key)) return code;
  }

  // Canada detection
  // Check for "CAN", "CANADA", or common province abbreviations/names found in hockey databases
  // Note: " AB" (Alberta), " BC" (British Columbia), " ON" (Ontario), etc.
  // Using spaces to avoid matching inside words (e.g. "CANT")
  // Also matching "ONT.", "MAN.", "ALB.", "SASK." based on screenshot
  if (h.includes('CAN') || h.includes('CANADA') ||
    h.includes(' ON') || h.includes('QUE') || h.includes(' BC') || h.includes(' AB') || h.includes(' MB') ||
    h.includes(' SK') || h.includes(' NS') || h.includes(' NB') || h.includes(' PE') || h.includes(' NL') ||
    h.includes('ONT') || h.includes('MAN') || h.includes('ALB') || h.includes('SASK')) {
    return 'CAN';
  }

  // Default to USA
  return 'USA';
}

app.get('/api/stats', async (req, res) => {
  // Include season in cache key to avoid conflicts between seasons
  const config = require('./config/scraper-config');
  const statsSeason = config.seasons.stats;
  const cacheKey = `chn_stats_${statsSeason}`;

  try {
    const cachedData = await getFromCache(cacheKey);
    if (cachedData) {
      console.log(`[Cache System] Stats data found in cache for ${cacheKey}.`);
      return res.json(cachedData);
    }

    console.log(`[Cache System] No cache found for ${cacheKey}. Scraping live data.`);
    const statsData = await scrapeCHNStats();
    if (statsData.skaters.length > 0 || statsData.goalies.length > 0) {
      await saveToCache(statsData, cacheKey);
      res.json(statsData);
    } else {
      res.status(500).json({ error: 'Failed to fetch stats data.' });
    }
  } catch (error) {
    console.error('Error in /api/stats endpoint:', error);
    res.status(500).json({ error: 'Internal server error while fetching stats.' });
  }
});

