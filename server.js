// Helper function to read and parse the hockey data JSON file
async function getHockeyData() {
  try {
    const rawData = await fs.readFile(HOCKEY_DATA_PATH, 'utf8');
    return JSON.parse(rawData);
  } catch (error) {
    if (error.code === 'ENOENT') {
      console.error('Error: asu_hockey_data.json not found.');
      return null; // Or throw new Error('Data file not found') if you want to ensure it always exists
    }
    console.error('Error reading or parsing asu_hockey_data.json:', error);
    return null; // Or throw error
  }
}

